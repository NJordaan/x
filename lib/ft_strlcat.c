#include "libft.h"

size_t	ft_strlcat(char *dst, char const *src, size_t size)
{
	size_t		i;
	size_t		d;
	size_t		s;

	i = 0;
	d = ft_strlen(dst);
	s = ft_strlen(src);
	if (size <= d)
		return (s + size);
	while ((dst[i] != '\0') && i < (size - 1))
		i++;
	while (*src && i < (size - 1))
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (d + s);
}
